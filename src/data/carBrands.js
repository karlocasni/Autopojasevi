// Helper function to generate year array from range
export function getYearsForModel(model) {
    const [startYear, endYear] = model.years;
    const years = [];
    for (let year = endYear; year >= startYear; year--) {
        years.push(year);
    }
    return years;
}

// Helper function to search brands
export function searchBrands(query) {
    if (!query) return carBrands;
    const lowerQuery = query.toLowerCase();
    return carBrands.filter(brand =>
        brand.name.toLowerCase().includes(lowerQuery)
    );
}
